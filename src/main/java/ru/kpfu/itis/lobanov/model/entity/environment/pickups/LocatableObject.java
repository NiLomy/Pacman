package ru.kpfu.itis.lobanov.model.entity.environment.pickups;

import java.io.Serializable;

public abstract class LocatableObject implements Serializable {
    protected double x;
    protected double y;

    public LocatableObject(double x, double y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        LocatableObject that = (LocatableObject) o;

        if (Double.compare(x, that.x) != 0) return false;
        return Double.compare(y, that.y) == 0;
    }

    @Override
    public int hashCode() {
        int result;
        long temp;
        temp = Double.doubleToLongBits(x);
        result = (int) (temp ^ (temp >>> 32));
        temp = Double.doubleToLongBits(y);
        result = 31 * result + (int) (temp ^ (temp >>> 32));
        return result;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
